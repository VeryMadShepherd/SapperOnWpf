namespace SapperOnWpf
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void StartButton_Click(object sender, RoutedEventArgs e)
        {
            int rows = int.Parse(RowsBox.Text);
            int columns = int.Parse(ColumnsBox.Text);
            int mines = int.Parse(MinesBox.Text);
            var sapperForm = new SapperForm(rows, columns, mines);
            sapperForm.Show();
        }

        private void AllowOnlyInteger(object sender, TextCompositionEventArgs e)
        {
            Regex regex = new Regex("[^0-9]+");
            e.Handled = regex.IsMatch(e.Text);
        }
    }
}
